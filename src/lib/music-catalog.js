export const TRACK_PRICE_CENTS = 200
export const BUNDLE_3_PRICE_CENTS = 500
export const BUNDLE_5_PRICE_CENTS = 800

export const ALL_TRACKS = ALBUMS.flatMap((album) => album.tracks)

export function getTrackBySlug(slug) {
  return ALL_TRACKS.find((track) => track.slug === slug)
}

export function getAlbumBySlug(slug) {
  return ALBUMS.find((album) => album.slug === slug)
}

export function getTracksForAlbum(albumSlug) {
  const album = getAlbumBySlug(albumSlug)
  return album ? album.tracks : []
}

export function uniqueTracksBySlug(tracks) {
  const seen = new Set()
  return tracks.filter((track) => {
    if (!track || seen.has(track.slug)) return false
    seen.add(track.slug)
    return true
  })
}

export function calculateTrackBundlePrice(trackCount) {
  let remaining = trackCount
  let total = 0

  while (remaining >= 5) {
    total += BUNDLE_5_PRICE_CENTS
    remaining -= 5
  }

  while (remaining >= 3) {
    total += BUNDLE_3_PRICE_CENTS
    remaining -= 3
  }

  total += remaining * TRACK_PRICE_CENTS
  return total
}

export function summarizeTrackPricing(trackCount) {
  let remaining = trackCount
  const summary = []

  while (remaining >= 5) {
    summary.push("5-track bundle")
    remaining -= 5
  }

  while (remaining >= 3) {
    summary.push("3-track bundle")
    remaining -= 3
  }

  for (let i = 0; i < remaining; i += 1) {
    summary.push("single track")
  }

  return summary
}

export function resolvePurchase({ trackSlugs = [], albumSlugs = [] }) {
  const selectedTracks = uniqueTracksBySlug(trackSlugs.map(getTrackBySlug).filter(Boolean))
  const selectedAlbums = albumSlugs.map(getAlbumBySlug).filter(Boolean)

  const albumTracks = uniqueTracksBySlug(
    selectedAlbums.flatMap((album) => album.tracks)
  )

  const allDeliveredTracks = uniqueTracksBySlug([...selectedTracks, ...albumTracks])

  const tracksPrice = calculateTrackBundlePrice(selectedTracks.length)
  const albumsPrice = selectedAlbums.reduce((sum, album) => sum + album.priceCents, 0)

  return {
    selectedTracks,
    selectedAlbums,
    deliveredTracks: allDeliveredTracks,
    totalCents: tracksPrice + albumsPrice,
    trackPricingSummary: summarizeTrackPricing(selectedTracks.length),
  }
}